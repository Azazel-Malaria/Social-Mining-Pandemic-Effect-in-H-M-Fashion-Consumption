#!/usr/bin/env bash
set -euo pipefail
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
ROOT_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"
cd "$ROOT_DIR"
export PYTHONPATH="$ROOT_DIR:${PYTHONPATH:-}"
source "$SCRIPT_DIR/_argparse.sh" "$@"
CMD=(python -m util.social_experiment
  --social_output_root "${social_output_root:-./social_output/k}"
  --data_root "${data_root:-./data}"
  --exp_id "${exp_id:-01}"
  --event_month "${event_month:-2020-03}"
  --style_dims "${style_dims:-formal,office,comfort,homewear,casual,value}"
  --covid_vars "${covid_vars:-covid_cases_index,covid_deaths_index,covid_stringency_index,covid_reproduction_rate}"
  --fourier_order "${fourier_order:-2}"
  --max_lag "${max_lag:-4}"
  --max_lead "${max_lead:-2}"
  --ar_lags "${ar_lags:-2}"
  --min_periods "${min_periods:-6}"
  --min_rows_panel "${min_rows_panel:-500}"
  --max_panel_rows "${max_panel_rows:-500000}"
  --event_window "${event_window:-8}"
  --outlier_bound "${outlier_bound:-2.5}"
  --n_clusters "${n_clusters:-4}"
  --hist_bins "${hist_bins:-40}"
  --max_distribution_samples "${max_distribution_samples:-50000}"
  --use_copula_scores "${use_copula_scores:-0}"
  --high_quantile "${high_quantile:-0.75}"
  --low_quantile "${low_quantile:-0.25}"
  --gmm_components "${gmm_components:-3}"
  --rf_trees "${rf_trees:-200}"
  --rf_max_depth "${rf_max_depth:-5}"
  --seed "${seed:-42}"
)
[[ -n "${exp_name:-}" ]] && CMD+=(--exp_name "$exp_name")
"${CMD[@]}"
